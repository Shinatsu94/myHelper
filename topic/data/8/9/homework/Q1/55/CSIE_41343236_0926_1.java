public class CSIE_41343236_0926_1 {
    public static void main(String[] args) {
        // 學校名稱
        char a = '虎';
        char b = '科';
        char c = '大';

        // 中文姓名
        char firstname = '游';
        char middlename = '定';
        char lastname = '叡';

        // 依序輸出學校名稱
        System.out.println(a + " " + (int) a);
        System.out.println(b + " " + (int) b);
        System.out.println(c + " " + (int) c);

        // 依序輸出中文姓名
        System.out.println(firstname + " " + (int) firstname);
        System.out.println(middlename + " " + (int) middlename);
        System.out.println(lastname + " " + (int) lastname);
    }
}
